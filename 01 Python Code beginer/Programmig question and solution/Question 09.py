#কোন একিট সংখ্যা ইনপুট নিয়ে এর নামতা প্রিন্ট করার প্রোগ্রাম লিখ?

#While loop ব্যবহার করে প্রোগ্রাম:

num = int(input("Enter the number:"))
i = 0
while (i<=9):
    i = i+1
    print(num, '*', i, "=",num*i)




#For loop ব্যবহার করে প্রোগ্রাম:

num = int(input("Display multiplication table of:"))
for i in range(1,11):
    print(num, "*", i, '=', num *i)








