x = float(input("Enter the number of Result:"))

if x>=80:
    print("The result is A+")
    
elif x>=70:
    print("The result is A")
    
elif x>=60:
    print("The result is A-")
    
elif x>=50:
    print("The result is B")
    
elif x>=40:
    print("The result is B-")
    
elif x>=33:
    print("The result is C")
    
else:
    print("The result is F")